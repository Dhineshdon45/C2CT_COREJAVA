package com.tns.ifet.dayone;

public class CharDemo {

	public static void main(String[] args) {
		char ch = 'd';
		System.out.println(ch);
		
		char ch1 = 73;
		System.out.println(ch1);
		
		char var1 = '\u00A3';
		System.out.println(var1);
		
		char var2 = '\u20AE';
		System.out.println(var2);
		
		int a = 'B';
		System.out.println(a);	

	}

}
